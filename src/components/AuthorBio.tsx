"use client";

import { type Author } from "@/src/lib/authors";
import Link from "next/link";

interface AuthorBioProps {
    author: Author;
    variant?: "full" | "compact" | "inline";
    showCredentials?: boolean;
    showSocial?: boolean;
}

export function AuthorBio({
    author,
    variant = "full",
    showCredentials = true,
    showSocial = true,
}: AuthorBioProps) {
    if (variant === "inline") {
        return (
            <div className="flex items-center gap-3">
                <div className="w-10 h-10 rounded-full bg-gradient-to-br from-ocean-100 to-ocean-200 flex items-center justify-center text-ocean-600 font-bold text-sm overflow-hidden">
                    {author.avatar ? (
                        <img
                            src={author.avatar}
                            alt={author.name}
                            className="w-full h-full object-cover"
                            onError={(e) => {
                                e.currentTarget.style.display = 'none';
                                e.currentTarget.nextElementSibling?.classList.remove('hidden');
                            }}
                        />
                    ) : null}
                    <span className={author.avatar ? "hidden" : ""}>
                        {author.name.split(" ").map(n => n[0]).join("")}
                    </span>
                </div>
                <div>
                    <Link
                        href={`/authors/${author.slug}`}
                        className="font-semibold text-gray-900 hover:text-ocean-600 transition-colors text-sm"
                    >
                        {author.name}
                    </Link>
                    <p className="text-xs text-gray-500">{author.title}</p>
                </div>
                {author.verifiedExpert && (
                    <span className="ml-2 inline-flex items-center gap-1 px-2 py-0.5 rounded-full bg-green-100 text-green-700 text-xs font-medium">
                        <svg className="w-3 h-3" fill="currentColor" viewBox="0 0 20 20">
                            <path fillRule="evenodd" d="M16.707 5.293a1 1 0 010 1.414l-8 8a1 1 0 01-1.414 0l-4-4a1 1 0 011.414-1.414L8 12.586l7.293-7.293a1 1 0 011.414 0z" clipRule="evenodd" />
                        </svg>
                        Verified Expert
                    </span>
                )}
            </div>
        );
    }

    if (variant === "compact") {
        return (
            <div className="flex items-start gap-4 p-4 rounded-xl bg-gradient-to-br from-gray-50 to-white border border-gray-100">
                <div className="w-14 h-14 rounded-full bg-gradient-to-br from-ocean-100 to-ocean-200 flex items-center justify-center text-ocean-600 font-bold text-lg overflow-hidden flex-shrink-0">
                    {author.avatar ? (
                        <img
                            src={author.avatar}
                            alt={author.name}
                            className="w-full h-full object-cover"
                            onError={(e) => {
                                e.currentTarget.style.display = 'none';
                            }}
                        />
                    ) : (
                        <span>{author.name.split(" ").map(n => n[0]).join("")}</span>
                    )}
                </div>
                <div className="flex-1 min-w-0">
                    <div className="flex items-center gap-2 flex-wrap">
                        <Link
                            href={`/authors/${author.slug}`}
                            className="font-bold text-gray-900 hover:text-ocean-600 transition-colors"
                        >
                            {author.name}
                        </Link>
                        {author.verifiedExpert && (
                            <span className="inline-flex items-center gap-1 px-2 py-0.5 rounded-full bg-green-100 text-green-700 text-xs font-medium">
                                <svg className="w-3 h-3" fill="currentColor" viewBox="0 0 20 20">
                                    <path fillRule="evenodd" d="M16.707 5.293a1 1 0 010 1.414l-8 8a1 1 0 01-1.414 0l-4-4a1 1 0 011.414-1.414L8 12.586l7.293-7.293a1 1 0 011.414 0z" clipRule="evenodd" />
                                </svg>
                                Verified
                            </span>
                        )}
                    </div>
                    <p className="text-sm text-gray-600 mt-0.5">{author.title}</p>
                    <p className="text-sm text-gray-500 mt-2">{author.shortBio}</p>
                    <div className="flex items-center gap-4 mt-3 text-xs text-gray-500">
                        <span className="flex items-center gap-1">
                            <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 12h6m-6 4h6m2 5H7a2 2 0 01-2-2V5a2 2 0 012-2h5.586a1 1 0 01.707.293l5.414 5.414a1 1 0 01.293.707V19a2 2 0 01-2 2z" />
                            </svg>
                            {author.articlesWritten} articles
                        </span>
                        <span className="flex items-center gap-1">
                            <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M12 8v4l3 3m6-3a9 9 0 11-18 0 9 9 0 0118 0z" />
                            </svg>
                            {author.yearsExperience}+ years
                        </span>
                    </div>
                </div>
            </div>
        );
    }

    // Full variant
    return (
        <div className="p-6 rounded-2xl bg-gradient-to-br from-gray-50 via-white to-ocean-50/30 border border-gray-100 shadow-sm">
            <div className="flex items-start gap-6">
                {/* Avatar */}
                <div className="w-20 h-20 rounded-full bg-gradient-to-br from-ocean-100 to-ocean-200 flex items-center justify-center text-ocean-600 font-bold text-2xl overflow-hidden flex-shrink-0 shadow-lg shadow-ocean-500/10">
                    {author.avatar ? (
                        <img
                            src={author.avatar}
                            alt={author.name}
                            className="w-full h-full object-cover"
                            onError={(e) => {
                                e.currentTarget.style.display = 'none';
                            }}
                        />
                    ) : (
                        <span>{author.name.split(" ").map(n => n[0]).join("")}</span>
                    )}
                </div>

                {/* Content */}
                <div className="flex-1">
                    <div className="flex items-center gap-3 flex-wrap">
                        <Link
                            href={`/authors/${author.slug}`}
                            className="text-xl font-bold text-gray-900 hover:text-ocean-600 transition-colors"
                        >
                            {author.name}
                        </Link>
                        {author.verifiedExpert && (
                            <span className="inline-flex items-center gap-1 px-2.5 py-1 rounded-full bg-green-100 text-green-700 text-xs font-semibold">
                                <svg className="w-3.5 h-3.5" fill="currentColor" viewBox="0 0 20 20">
                                    <path fillRule="evenodd" d="M16.707 5.293a1 1 0 010 1.414l-8 8a1 1 0 01-1.414 0l-4-4a1 1 0 011.414-1.414L8 12.586l7.293-7.293a1 1 0 011.414 0z" clipRule="evenodd" />
                                </svg>
                                Verified Expert
                            </span>
                        )}
                    </div>
                    <p className="text-ocean-600 font-medium mt-1">{author.title}</p>

                    {/* Stats */}
                    <div className="flex items-center gap-6 mt-3 text-sm text-gray-600">
                        <span className="flex items-center gap-1.5">
                            <svg className="w-4 h-4 text-gray-400" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 12h6m-6 4h6m2 5H7a2 2 0 01-2-2V5a2 2 0 012-2h5.586a1 1 0 01.707.293l5.414 5.414a1 1 0 01.293.707V19a2 2 0 01-2 2z" />
                            </svg>
                            <strong>{author.articlesWritten}</strong> articles
                        </span>
                        <span className="flex items-center gap-1.5">
                            <svg className="w-4 h-4 text-gray-400" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M12 8v4l3 3m6-3a9 9 0 11-18 0 9 9 0 0118 0z" />
                            </svg>
                            <strong>{author.yearsExperience}+</strong> years experience
                        </span>
                    </div>

                    {/* Bio */}
                    <p className="mt-4 text-gray-600 leading-relaxed">{author.bio}</p>

                    {/* Credentials */}
                    {showCredentials && author.credentials.length > 0 && (
                        <div className="mt-4">
                            <h4 className="text-xs font-semibold text-gray-500 uppercase tracking-wide mb-2">Credentials</h4>
                            <div className="flex flex-wrap gap-2">
                                {author.credentials.map((cred) => (
                                    <span key={cred} className="px-2.5 py-1 rounded-full bg-ocean-50 text-ocean-700 text-xs font-medium border border-ocean-100">
                                        {cred}
                                    </span>
                                ))}
                            </div>
                        </div>
                    )}

                    {/* Social Links */}
                    {showSocial && Object.keys(author.socialLinks).length > 0 && (
                        <div className="mt-4 flex items-center gap-3">
                            {author.socialLinks.twitter && (
                                <a
                                    href={author.socialLinks.twitter}
                                    target="_blank"
                                    rel="noopener noreferrer"
                                    className="w-8 h-8 rounded-full bg-gray-100 hover:bg-gray-200 flex items-center justify-center text-gray-600 transition-colors"
                                    aria-label="Twitter"
                                >
                                    <svg className="w-4 h-4" fill="currentColor" viewBox="0 0 24 24">
                                        <path d="M18.244 2.25h3.308l-7.227 8.26 8.502 11.24H16.17l-5.214-6.817L4.99 21.75H1.68l7.73-8.835L1.254 2.25H8.08l4.713 6.231zm-1.161 17.52h1.833L7.084 4.126H5.117z" />
                                    </svg>
                                </a>
                            )}
                            {author.socialLinks.linkedin && (
                                <a
                                    href={author.socialLinks.linkedin}
                                    target="_blank"
                                    rel="noopener noreferrer"
                                    className="w-8 h-8 rounded-full bg-gray-100 hover:bg-gray-200 flex items-center justify-center text-gray-600 transition-colors"
                                    aria-label="LinkedIn"
                                >
                                    <svg className="w-4 h-4" fill="currentColor" viewBox="0 0 24 24">
                                        <path d="M20.447 20.452h-3.554v-5.569c0-1.328-.027-3.037-1.852-3.037-1.853 0-2.136 1.445-2.136 2.939v5.667H9.351V9h3.414v1.561h.046c.477-.9 1.637-1.85 3.37-1.85 3.601 0 4.267 2.37 4.267 5.455v6.286zM5.337 7.433c-1.144 0-2.063-.926-2.063-2.065 0-1.138.92-2.063 2.063-2.063 1.14 0 2.064.925 2.064 2.063 0 1.139-.925 2.065-2.064 2.065zm1.782 13.019H3.555V9h3.564v11.452zM22.225 0H1.771C.792 0 0 .774 0 1.729v20.542C0 23.227.792 24 1.771 24h20.451C23.2 24 24 23.227 24 22.271V1.729C24 .774 23.2 0 22.222 0h.003z" />
                                    </svg>
                                </a>
                            )}
                            {author.socialLinks.instagram && (
                                <a
                                    href={author.socialLinks.instagram}
                                    target="_blank"
                                    rel="noopener noreferrer"
                                    className="w-8 h-8 rounded-full bg-gray-100 hover:bg-gray-200 flex items-center justify-center text-gray-600 transition-colors"
                                    aria-label="Instagram"
                                >
                                    <svg className="w-4 h-4" fill="currentColor" viewBox="0 0 24 24">
                                        <path d="M12 2.163c3.204 0 3.584.012 4.85.07 3.252.148 4.771 1.691 4.919 4.919.058 1.265.069 1.645.069 4.849 0 3.205-.012 3.584-.069 4.849-.149 3.225-1.664 4.771-4.919 4.919-1.266.058-1.644.07-4.85.07-3.204 0-3.584-.012-4.849-.07-3.26-.149-4.771-1.699-4.919-4.92-.058-1.265-.07-1.644-.07-4.849 0-3.204.013-3.583.07-4.849.149-3.227 1.664-4.771 4.919-4.919 1.266-.057 1.645-.069 4.849-.069zM12 0C8.741 0 8.333.014 7.053.072 2.695.272.273 2.69.073 7.052.014 8.333 0 8.741 0 12c0 3.259.014 3.668.072 4.948.2 4.358 2.618 6.78 6.98 6.98C8.333 23.986 8.741 24 12 24c3.259 0 3.668-.014 4.948-.072 4.354-.2 6.782-2.618 6.979-6.98.059-1.28.073-1.689.073-4.948 0-3.259-.014-3.667-.072-4.947-.196-4.354-2.617-6.78-6.979-6.98C15.668.014 15.259 0 12 0zm0 5.838a6.162 6.162 0 100 12.324 6.162 6.162 0 000-12.324zM12 16a4 4 0 110-8 4 4 0 010 8zm6.406-11.845a1.44 1.44 0 100 2.881 1.44 1.44 0 000-2.881z" />
                                    </svg>
                                </a>
                            )}
                        </div>
                    )}
                </div>
            </div>
        </div>
    );
}
